// One-shot generator for drizzle/0065_cannon_v2.sql — inlines the canonical
// season seed JSON (apps/showcase-cannon/src/season/*.json) as app_feeds rows
// so the SQL never drifts from the app's bundled fallback by hand-editing.
// Re-run after editing the season JSON if the migration has not shipped yet.
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const seasonDir = path.join(here, '../../showcase-cannon/src/season');
const out = path.join(here, '../drizzle/0065_cannon_v2.sql');

const FEEDS = [
  ['fixtures', 'cannon.fixtures.v1', 'fixtures.json'],
  ['match-live', 'cannon.match.v1', 'match.json'],
  ['squad', 'cannon.squad.v1', 'squad.json'],
  ['news', 'cannon.news.v1', 'news.json'],
  ['club', 'cannon.club.v1', 'club.json'],
];

const DDL = `-- The Cannon v2 — flagship season upgrade.
-- 1) Terrace takes gain match scoping + moderation state.
-- 2) cannon_reports: community report path (one per anon key per take).
-- 3) cannon_predictions: pre-match W/D/L picks (the live fan-confidence bar).
-- 4) Seed the five Cannon feeds (Feed Protocol, migration 0056) so the app
--    has server truth from first boot. Publishes overwrite these snapshots.
-- Generated by scripts/gen-cannon-migration.mjs from showcase-cannon/src/season/*.json.

ALTER TABLE cannon_takes ADD COLUMN match_id TEXT;
--> statement-breakpoint
ALTER TABLE cannon_takes ADD COLUMN status TEXT NOT NULL DEFAULT 'visible';
--> statement-breakpoint
ALTER TABLE cannon_takes ADD COLUMN report_count INTEGER NOT NULL DEFAULT 0;
--> statement-breakpoint
CREATE INDEX cannon_takes_match_idx ON cannon_takes (match_id, created_at DESC);
--> statement-breakpoint
CREATE TABLE cannon_reports (
  take_id TEXT NOT NULL,
  anon_key TEXT NOT NULL,
  reason TEXT NOT NULL,
  created_at INTEGER NOT NULL,
  PRIMARY KEY (take_id, anon_key)
);
--> statement-breakpoint
CREATE TABLE cannon_predictions (
  match_id TEXT NOT NULL,
  anon_key TEXT NOT NULL,
  pick TEXT NOT NULL,
  created_at INTEGER NOT NULL,
  PRIMARY KEY (match_id, anon_key)
);
--> statement-breakpoint
CREATE INDEX cannon_predictions_match_idx ON cannon_predictions (match_id);
`;

const esc = (s) => s.replaceAll("'", "''");
const NOW_MS = 1781136000000; // 2026-06-11T00:00:00Z — fixed for determinism
let sql = DDL;
for (const [feedId, schema, file] of FEEDS) {
  const payload = JSON.parse(fs.readFileSync(path.join(seasonDir, file), 'utf8'));
  const compact = JSON.stringify(payload);
  sql +=
    `--> statement-breakpoint\n` +
    `INSERT OR IGNORE INTO app_feeds (id, app_slug, feed_id, data_schema, sequence, updated_at, stale_after, hash, source_kind, source_name, payload, created_at)\n` +
    `VALUES ('cannon:${feedId}', 'cannon', '${feedId}', '${schema}', 1, '2026-06-11T00:00:00.000Z', NULL, 'fnv1a:00000000', 'manual', 'launch-seed', '${esc(compact)}', ${NOW_MS});\n`;
}
fs.writeFileSync(out, sql);
console.log(`wrote ${out} (${sql.length} bytes)`);
