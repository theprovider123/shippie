// authjs-to-local-identity
// ---------------------------------------------------------------------------

const NEXTAUTH_IMPORT = /import\s+([^;]*)\s+from\s+['"]next-auth(\/[^'"]+)?['"]\s*;?/g;
const AUTHJS_IMPORT = /import\s+([^;]*)\s+from\s+['"]@auth\/[^'"]+['"]\s*;?/g;
const GET_SESSION_CALL = /(?:await\s+)?(?:getServerSession|getSession|auth)\s*\([^)]*\)/g;

function localizeAuthjs(files: ReadonlyMap<string, Uint8Array>): LocalizePatch {
  const fileChanges: FileChange[] = [];
  const warnings: string[] = [];

  for (const [path, bytes] of files) {
    if (!isJsTs(path)) continue;
    const before = decoder.decode(bytes);
    if (!/next-auth|@auth\//.test(before)) continue;

    let after = before;
    after = after.replace(
      NEXTAUTH_IMPORT,
      `import { getLocalIdentitySession } from '$lib/shippie-local/local-identity';`,
    );
    after = after.replace(
      AUTHJS_IMPORT,
      `import { getLocalIdentitySession } from '$lib/shippie-local/local-identity';`,
    );
    after = after.replace(GET_SESSION_CALL, 'await getLocalIdentitySession()');

    // Heuristic: signIn(provider) calls won't translate.
    if (/signIn\s*\(/.test(after)) {
      warnings.push(`${path}: contains signIn() — local identity has no providers. Remove the call site.`);
    }
    if (after === before) continue;
    fileChanges.push({ path, before, after });
  }

  const newFiles: NewFile[] = [];
  if (fileChanges.length > 0) {
    newFiles.push({
      path: `${SHIM_DIR}/local-identity.ts`,
      content: LOCAL_IDENTITY_SRC,
    });
  }

  return {
    transform: 'authjs-to-local-identity',
    fileChanges,
    newFiles,
    warnings,
  };
}

const LOCAL_IDENTITY_SRC = `/**
 * Shippie Local Identity shim.
 *
 * Replaces Auth.js / NextAuth providers with a per-install identity.
 * The user is "always logged in" as themselves on their own device —
 * no provider redirects, no session table, no JWTs. Their data is
 * already private because it lives on their phone.
 *
 * Generated by Localize.
 */
const STORAGE_KEY = 'shippie:local-identity:v1';

export interface LocalIdentitySession {
  user: {
    id: string;
    name: string;
    email: null;
  };
  expires: null;
}

function getOrCreateId(): string {
  if (typeof localStorage === 'undefined') return 'device-local';
  const cached = localStorage.getItem(STORAGE_KEY);
  if (cached && cached.length >= 8) return cached;
  const bytes = new Uint8Array(16);
  crypto.getRandomValues(bytes);
  let hex = '';
  for (const b of bytes) hex += b.toString(16).padStart(2, '0');
  const id = 'device-local:' + hex;
  try {
    localStorage.setItem(STORAGE_KEY, id);
  } catch {
    /* private mode — ephemeral id */
  }
  return id;
}

export async function getLocalIdentitySession(): Promise<LocalIdentitySession> {
  return {
    user: { id: getOrCreateId(), name: 'You', email: null },
    expires: null,
  };
}
`;

// ---------------------------------------------------------------------------
// supabase-storage-to-local-files
// ---------------------------------------------------------------------------

const STORAGE_UPLOAD = /\.storage\.from\s*\(\s*['"]([^'"]+)['"]\s*\)\s*\.upload\s*\(\s*['"]([^'"]+)['"]\s*,\s*([^)]+)\)/g;
const STORAGE_PUBLIC_URL = /\.storage\.from\s*\(\s*['"]([^'"]+)['"]\s*\)\s*\.getPublicUrl\s*\(\s*['"]([^'"]+)['"]\s*\)/g;

function localizeSupabaseStorage(files: ReadonlyMap<string, Uint8Array>): LocalizePatch {
  const fileChanges: FileChange[] = [];
  const warnings: string[] = [];

  for (const [path, bytes] of files) {
    if (!isJsTs(path)) continue;
    const before = decoder.decode(bytes);
    if (!/\.storage\.from\(/.test(before)) continue;

    let after = before;
    after = after.replace(STORAGE_UPLOAD, (_m, bucket, key, body) => {
      return `(await import('$lib/shippie-local/local-files')).writeLocalFile('${bucket}/${key}', ${body.trim()})`;
    });
    after = after.replace(STORAGE_PUBLIC_URL, (_m, bucket, key) => {
      return `await (await import('$lib/shippie-local/local-files')).localFileUrl('${bucket}/${key}')`;
    });

    if (after === before) continue;
    fileChanges.push({ path, before, after });
  }

  const newFiles: NewFile[] = [];
  if (fileChanges.length > 0) {
    newFiles.push({
      path: `${SHIM_DIR}/local-files.ts`,
      content: LOCAL_FILES_SRC,
    });
  }

  return {
    transform: 'supabase-storage-to-local-files',
    fileChanges,
    newFiles,
    warnings,
  };
}

const LOCAL_FILES_SRC = `/**
 * Shippie local-files shim.
 *
 * Replaces Supabase Storage uploads / signed URLs with on-device file
 * storage via OPFS. Generated by Localize.
 */
import { noteLocalWrite } from '@shippie/sdk/wrapper';

async function root(): Promise<FileSystemDirectoryHandle> {
  return await navigator.storage.getDirectory();
}

async function ensureDir(parts: string[]): Promise<FileSystemDirectoryHandle> {
  let dir = await root();
  for (const part of parts) {
    dir = await dir.getDirectoryHandle(part, { create: true });
  }
  return dir;
}

export async function writeLocalFile(
  path: string,
  body: Blob | ArrayBuffer | Uint8Array | string,
): Promise<{ data: { path: string }; error: null }> {
  const segs = path.split('/');
  const name = segs.pop()!;
  const dir = await ensureDir(segs);
  const handle = await dir.getFileHandle(name, { create: true });
  const writable = await handle.createWritable();
  await writable.write(body);
  await writable.close();
  noteLocalWrite({ kind: 'file', path });
  return { data: { path }, error: null };
}

export async function localFileUrl(
  path: string,
): Promise<{ data: { publicUrl: string } }> {
  const segs = path.split('/');
  const name = segs.pop()!;
  const dir = await ensureDir(segs);
  const handle = await dir.getFileHandle(name);
  const file = await handle.getFile();
  return { data: { publicUrl: URL.createObjectURL(file) } };
}
`;

// ---------------------------------------------------------------------------
// helpers
