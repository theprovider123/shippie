function isJsTs(path: string): boolean {
  return (
    path.endsWith('.js') ||
    path.endsWith('.mjs') ||
    path.endsWith('.cjs') ||
    path.endsWith('.ts') ||
    path.endsWith('.tsx') ||
    path.endsWith('.jsx')
  );
}
